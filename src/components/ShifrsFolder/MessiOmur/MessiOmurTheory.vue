    <p>5. <strong>m_1</strong>: Результат шифрования сообщения <em>m</em> с использованием ключа <strong>e_A</strong>. Это значение будет передано Bob'у.</p>
    <p>6. <strong>m_2</strong>: Результат дополнительного шифрования <em>m_1</em> с использованием ключа <strong>e_B</strong>. Это значение будет отправлено обратно Alice.</p>
    <p>7. <strong>m_3</strong>: Значение, которое Alice получит, когда она снимет свой замок, используя ключ <strong>d_B</strong>.</p>
