            <li>Абоненту Bob доставляется секретное сообщение <em>m</em> от Alice.</li>
        </ol>

        <h2>Классический вариант: Пример работы алгоритма</h2>
        <p>Предположим, что Alice хочет отправить сообщение <strong>m = 15</strong> Bob'у, и параметры выбраны следующим образом:</p>

