        <h2>Эллиптический вариант</h2>
        <p>Эллиптический вариант криптосистемы Мэсси — Омуры позволяет передавать сообщение по открытому каналу без предварительной передачи ключевой информации. Системные параметры включают уравнение эллиптической кривой и поле <em>F</em>.</p>

        <h3>Эллиптический вариант: Алгоритм</h3>
        <ol>
            <li>Абонент Alice помещает сообщение <em>m</em> в точку <em>M(m)</em> на эллиптической кривой и умножает на свой секретный ключ <em>e_A</em>, получая точку <em>P_1</em>.</li>
            <li>Bob вычисляет <em>P_2 = e_B * P_1</em> и отправляет результат Alice.</li>
            <li>Alice снимает замок, вычисляя <em>P_3 = d_A * P_2</em>.</li>
            <li>Расшифровывание происходит с использованием секретного ключа <em>d_B</em>: <em>M(m) = d_B * P_3</em>.</li>
        </ol>

        <h3>Эллиптический вариант: Пример работы алгоритма</h3>

    <p>Предположим, что Alice хочет отправить сообщение <strong>m = 15</strong> Bob'у, и параметры выбраны следующим образом:</p>

    <ul>
        <li><strong>ε</strong>: Уравнение эллиптической кривой.</li>
        <li><strong>F</strong>: Поле, задаваемое неприводимым многочленом.</li>
        <li><strong>N</strong>: Порядок эллиптической кривой.</li>
        <li><strong>e</strong>: Целое число, взаимно простое с <strong>N</strong>.</li>
    </ul>

    <h2>Переменные:</h2>
    <p>1. <strong>ε</strong>: Уравнение эллиптической кривой. В данном примере, предположим <strong>ε = y^2 = x^3 + ax + b</strong>.</p>
    <p>2. <strong>F</strong>: Поле, задаваемое неприводимым многочленом. Например, <strong>F = Z_p</strong> (где <strong>p</strong> — простое число).</p>
    <p>3. <strong>N</strong>: Порядок эллиптической кривой. Например, <strong>N = 19</strong>.</p>
    <p>4. <strong>e</strong>: Целое число, взаимно простое с <strong>N</strong>. Например, <strong>e = 5</strong>.</p>
    <p>5. <strong>d</strong>: Значение, найденное с использованием алгоритма Евклида, равное <strong>e^{-1} (mod N)</strong>.</p>
    <p>6. <strong>P</strong>: Некоторая точка на эллиптической кривой, представляющая сообщение <strong>m</strong>.</p>
    <p>7. <strong>P_1</strong>: Результат умножения точки <strong>P</strong> на ключ <strong>e</strong>.</p>
    <p>8. <strong>P_2</strong>: Результат умножения точки <strong>P_1</strong> на ключ <strong>e</strong>.</p>
    <p>9. <strong>Q</strong>: Результат умножения точки <strong>P</strong> на ключ <strong>d</strong>.</p>
    <p>10. <strong>R</strong>: Результат умножения точки <strong>Q</strong> на ключ <strong>e</strong>.</p>

    <h2>Шаги алгоритма:</h2>
    <ol>
        <li>Выбор уравнения эллиптической кривой <strong>ε</strong> и поля <strong>F</strong>.</li>
        <li>Выбор целого числа <strong>e</strong>, взаимно простого с порядком <strong>N</strong>, и нахождение <strong>d = e^{-1} (mod N)</strong>.</li>
        <li>Абонент Alice помещает свое сообщение <strong>m</strong> в точку <strong>P</strong> на эллиптической кривой.</li>
        <li>Умножение точки <strong>P</strong> на ключ <strong>e</strong>: <strong>P_1 = e * P</strong>.</li>
        <li>Умножение точки <strong>P_1</strong> на ключ <strong>e</strong>: <strong>P_2 = e * P_1</strong>.</li>
        <li>Умножение точки <strong>P</strong> на ключ <strong>d</strong>: <strong>Q = d * P</strong>.</li>
        <li>Умножение точки <strong>Q</strong> на ключ <strong>e</strong>: <strong>R = e * Q</strong>.</li>
    </ol>

    <p>Итак, сообщение <strong>m = 15</strong> успешно передано от Alice к Bob'у с использованием эллиптического варианта криптосистемы Мэсси — Омуры.</p>


