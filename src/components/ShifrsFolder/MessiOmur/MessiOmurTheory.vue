        <h2>Классический вариант: Алгоритм</h2>
        <ol>
            <li>Выбирается большое простое число <em>p</em> в качестве системного параметра.</li>
            <li>Абоненты Alice и Bob выбирают случайные числа <em>e_A</em> и <em>e_B</em> (взаимно простые с <em>φ(p)</em>, где <em>φ</em> - функция Эйлера).</li>
