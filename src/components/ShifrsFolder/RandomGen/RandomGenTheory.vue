    <h2>Генерация ключа:</h2>
    <p>Бит на позиции 6 выходит из регистра, и это становится ключевым битом.</p>
    
    <p>Процесс продолжается далее, генерируя последовательность ключевых битов на основе сдвигов и операций XOR.</p>

